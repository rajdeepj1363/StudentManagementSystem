package com.students.POJO;

public class TeacherInfo {
	private String name;
	private String mobile;
	private String address;
	private String dob;
	private String qualification;
	private String designation;
	private String classTeacher;
	private String teachingDivisions;
	private String teachingSubjects;
	private String uname;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getClassTeacher() {
		return classTeacher;
	}
	public void setClassTeacher(String classTeacher) {
		this.classTeacher = classTeacher;
	}
	public String getTeachingDivisions() {
		return teachingDivisions;
	}
	public void setTeachingDivisions(String teachingDivisions) {
		this.teachingDivisions = teachingDivisions;
	}
	public String getTeachingSubjects() {
		return teachingSubjects;
	}
	public void setTeachingSubjects(String teachingSubjects) {
		this.teachingSubjects = teachingSubjects;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	
}
